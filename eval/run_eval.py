"""Runs the Orchestrator against every case in ``test_cases.json`` (zero API calls -- all 12
cases use injection-mode documents) and writes ``docs/EVAL_REPORT.md`` with, per case: the
expected vs. actual result, a PASS/FAIL verdict per quantitative and qualitative check, the full
explainability trace, and the raw decision JSON.

Usage::

    python eval/run_eval.py
"""
from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path

from app.core.logging_config import get_logger
from app.core.orchestrator import Orchestrator
from app.models.common import ClaimInput
from app.storage.claims_ledger import ClaimsLedger
from app.storage.policy_repository import PolicyRepository
from eval.eval_helpers import CheckResult, evaluate_case, format_trace

_log = get_logger(__name__)

ROOT = Path(__file__).resolve().parents[1]
TEST_CASES_PATH = ROOT / "eval" / "test_cases.json"
REPORT_PATH = ROOT / "docs" / "EVAL_REPORT.md"


def run_all() -> tuple[list[dict], list]:
    with open(TEST_CASES_PATH, "r", encoding="utf-8") as f:
        test_cases = json.load(f)["test_cases"]

    _log.info("[eval] Run started — %d test cases", len(test_cases))
    policy_repo = PolicyRepository()
    rows = []
    for case in test_cases:
        _log.info("[eval] Running %s — %s", case["case_id"], case["case_name"])
        # Each case gets its own throwaway ledger so that one case's claims_history can't leak
        # into another's fraud check, and the eval run never mutates data/claims_ledger.json.
        ledger = ClaimsLedger(ROOT / "data" / f"_eval_ledger_{case['case_id']}.json")
        orchestrator = Orchestrator(policy_repository=policy_repo, claims_ledger=ledger)

        claim = ClaimInput.model_validate(case["input"])
        result = orchestrator.process_claim(claim, claim_ref=case["case_id"], record_in_ledger=False)

        checks = evaluate_case(case, result)
        passed = all(c.passed for c in checks)
        _log.info("[eval] %s %s — %d/%d checks passed", "PASS" if passed else "FAIL", case["case_id"], sum(1 for c in checks if c.passed), len(checks))
        rows.append({"case": case, "result": result, "checks": checks})

        ledger._path.unlink(missing_ok=True)

    total_passed = sum(1 for r in rows if all(c.passed for c in r["checks"]))
    _log.info("[eval] Run complete — %d/%d cases fully passed", total_passed, len(rows))
    return test_cases, rows


def _check_icon(check: CheckResult) -> str:
    return "✅" if check.passed else "❌"


def render_report(rows: list[dict]) -> str:
    total_checks = sum(len(r["checks"]) for r in rows)
    passed_checks = sum(1 for r in rows for c in r["checks"] if c.passed)
    cases_fully_passed = sum(1 for r in rows if all(c.passed for c in r["checks"]))

    lines: list[str] = []
    lines.append("# Evaluation Report")
    lines.append("")
    lines.append(f"Generated by `eval/run_eval.py` on {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}.")
    lines.append("")
    lines.append(f"**{cases_fully_passed}/{len(rows)} cases pass every check** ({passed_checks}/{total_checks} individual checks passed).")
    lines.append("")
    lines.append("All 12 cases run in *injection mode* (`DocumentInput.content` / `actual_type` / `quality` supplied "
                  "directly from `test_cases.json`), so this report is fully deterministic and makes **zero Gemini API calls**.")
    lines.append("")

    # Summary table
    lines.append("## Summary")
    lines.append("")
    lines.append("| Case | Name | Expected Decision | Actual Decision | Result |")
    lines.append("|---|---|---|---|---|")
    for row in rows:
        case, result, checks = row["case"], row["result"], row["checks"]
        expected_decision = case.get("expected", {}).get("decision")
        expected_str = "stopped early (no decision)" if expected_decision is None and "decision" in case.get("expected", {}) else str(expected_decision)
        if result.stopped_early:
            actual_str = "stopped early (no decision)"
        else:
            actual_str = result.decision.decision if result.decision else "—"
        verdict = "✅ PASS" if all(c.passed for c in checks) else "❌ FAIL"
        lines.append(f"| {case['case_id']} | {case['case_name']} | {expected_str} | {actual_str} | {verdict} |")
    lines.append("")

    # Per-case detail
    for row in rows:
        case, result, checks = row["case"], row["result"], row["checks"]
        lines.append(f"## {case['case_id']} — {case['case_name']}")
        lines.append("")
        lines.append(f"*{case['description']}*")
        lines.append("")
        lines.append(f"**Expected:** `{json.dumps(case.get('expected', {}), ensure_ascii=False)}`")
        lines.append("")

        overall = "✅ PASS" if all(c.passed for c in checks) else "❌ FAIL"
        lines.append(f"**Result: {overall}**")
        lines.append("")
        for c in checks:
            lines.append(f"- {_check_icon(c)} **{c.label}** — {c.detail}")
        lines.append("")

        if result.stopped_early:
            lines.append(f"**Member message:** {result.member_message}")
            lines.append("")
        elif result.decision:
            d = result.decision
            lines.append(f"**Decision:** `{d.decision}`  |  **Approved amount:** ₹{d.approved_amount:,.2f}  |  **Confidence:** {d.confidence_score:.2f}")
            if d.manual_review_recommended:
                lines.append(f"**Manual review recommended:** {', '.join(d.manual_review_reasons)}")
            lines.append("")

        lines.append("<details><summary>Explainability trace</summary>")
        lines.append("")
        for entry in format_trace(result.trace):
            lines.append(f"- {entry}")
        lines.append("")
        lines.append("</details>")
        lines.append("")

        lines.append("<details><summary>Full decision output (JSON)</summary>")
        lines.append("")
        lines.append("```json")
        payload = result.model_dump(mode="json", exclude={"trace"})
        lines.append(json.dumps(payload, indent=2, ensure_ascii=False))
        lines.append("```")
        lines.append("")
        lines.append("</details>")
        lines.append("")

    return "\n".join(lines)


def main() -> None:
    _test_cases, rows = run_all()
    report = render_report(rows)
    REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    REPORT_PATH.write_text(report, encoding="utf-8")

    cases_fully_passed = sum(1 for r in rows if all(c.passed for c in r["checks"]))
    print(f"{cases_fully_passed}/{len(rows)} cases pass every check. Report written to {REPORT_PATH}")
    for row in rows:
        failed = [c for c in row["checks"] if not c.passed]
        if failed:
            print(f"  {row['case']['case_id']}: {len(failed)} check(s) failed")
            for c in failed:
                print(f"    - {c.label}: {c.detail}")


if __name__ == "__main__":
    main()
